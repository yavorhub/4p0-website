			<p class="paragraph-heading-beta">Anforderungen in Story-Cards</p>
			Konkrete Anforderungen und einzelne Funktionalitäten werden in den Worten des Auftraggebers formuliert. Damit wird sichtbar, welche Ressourcen das Projekt braucht.
			Qualifikationen im Projektteam werden definiert und der grobe Zeitaufwand für die Umsetzung festgehalten.
			<p class="paragraph-heading-beta">Arbeitspakete im Produkt-Backlog</p>
			Die Anforderungen des Auftraggebers werden grob formuliert. Im Projektverlauf werden diese immer genauer definiert und angepasst.
			Daraus ergibt sich das Produkt-Backlog, eine geordnete Auflistung der Anforderungen an das Produkt.
			<p class="paragraph-heading-beta">Prioritäten</p>
			Im nächsten Schritt werden Prioritäten definiert: Welche Elemente und Funktionen sind am wichtigsten, um die Zufriedenheit des Auftraggebers zu gewährleisten?
			<p class="paragraph-heading-beta">Sprintplanung</p>
			In der ersten Besprechung zur Sprintplanung werden die Rahmenbedingungen abgesteckt. Wo und wann findet ein Team-meeting statt? Welches sind die Teilziele,
			welches die Meilensteine? In welche Teilaufgaben (Sprints) lässt sich das Projekt gliedern? Welche Projektdetails und welche Schnittstellen sind einzuhalten?
			Die Antworten auf diese Fragen bilden die Spielregeln und die Struktur für das weitere Vorgehen.
			<p class="paragraph-heading-beta">Aufgaben im Sprint-Backlog</p>
			Jeder Sprint wir in einzelne Aufgaben, Tickets genannt, unterteilt. Alle Tickets sind im sog. Sprint-Backlog aufgeführt. Das ist der Aufgabenplan – er zeigt
			den Arbeitsvorrat für das Team für den nächsten Arbeitszyklus, den Sprint. Jedes Teammitglied übernimmt eigenverantwortlich einzelne Tickets.
			<p class="paragraph-heading-beta">Arbeitsprozess im Sprint</p>
			Im Sprint arbeiten die Teammitglieder an ihren Aufgaben, den Tickets.
			<p class="paragraph-heading-beta">Besprechung im Scrum</p>
			Während eines täglichen fünfzehnminütigen Treffens berichtet jeder der Reihe nach, was er seit der letzten Besprechung gemacht hat und was er bis zur nächsten tun wird.
			In diesen Treffen werden auch Themen angesprochen, welche die Mitglieder bei der Arbeit daran hindern, effizient voranzukommen. Der Scrum-Master muss die Hindernisse aufgreifen und helfen,
			sie zu beseitigen.

			<p class="paragraph-heading-beta">Fortschrittkontrolle im Sprint-Review-Meeting</p>
			Jeder Sprint wird durch ein sog. Sprint-Review-Meeting abgeschlossen: Das Team stellt seine Ergebnisse dem Auftraggeber vor. Diese werden akzeptiert, oder es werden Anpassungen besprochen.
			In gesonderten Treffen können die Teammitglieder ihre Zusammenarbeit überprüfen und festhalten, was intern verbessert werden sollte. Die Erkenntnisse werden für zukünftige Projekte genutzt.
			So wird ein Lernprozess unterstützt.

			<p class="paragraph-heading-beta">Projektabschluss</p>
			Alle Sprints sind durchlaufen, das Projekt ist abgeschlossen.
			<p class="paragraph-heading-beta">Projektabschluss</p>

			<p>Zur Visualisierung der zu bearbeitenden Teilaufgaben, der Tickets, dient das sog. Task-Board. Hier wird für alle Teammitglieder der Projektfortschritt aufgezeigt:
				– Aufgabe – begonnen – in Bearbeitung – zu prüfen – erledigt. Das Team steuert sich und den Projektfortschritt selbstständig. </p>
		</div>
		<div class="article-post-column3" style="" >
			
			<p><span class="empty-space-para"></span>Das Sprint-Backlog, der Aufgabenplan, hängt für alle sichtbar an der Wand.
			Mit dem Task-Board kann der Projektablauf gesteuert werden. Es werden nur so viele Tickets für die Bearbeitung freigegeben, wie vom Projektteam bearbeitet werden können. Engpässe werden sichtbar,
				und eilige Tickets können vorgezogen werden. </p>
			<p><span class="empty-space-para"></span>Für das Kosten- und Risikomanagement wird ein Burndown-Chart verwendet.
			Er zeigt, ob das Projekt im ursprünglich vereinbarten Zeitund Kostenrahmen bleibt. Durch die täglichen Besprechun- gen werden Budgetverbrauch und Projektfortschritt sehr aktuell abgebildet.
				Mögliche Korrekturen können eingeleitet werden.</p>
