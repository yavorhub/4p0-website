			<p class="paragraph-heading-alpha">Mut zu Neuem zahlt sich aus</p>
			<p>Aus Scheu vor Neuem wird agiles Projektmanagement oft als Risiko betrachtet. Doch Agilität, also Flexibilität und Beweglichkeit, verkürzt die Reaktionszeit,was grundsätzlich ein Vorteil ist.
				Risiken liegen eher im Bereich zu grosser Teams (wegen des zu grossen Abstimmungsaufwands), zu langer Sprints und parallel zu bearbeitender Projekte.</p>
			<p><span class="empty-space-para"></span>
			Agile Projektbearbeitung hat einen positiven Einfluss auf den Projektablauf. Durch die enge Einbindung des Auftraggebers intensiviert sich die Kundennähe. Der Austausch und die
			Kommunikation mit den anderen Beteiligten wie z.B. Architekten führen in der agilen Projektbearbeitung zu Vorteilen, da jeder über den aktuellen Projektstand informiert ist und weiss,
			welche Aufgaben noch zu erledigen sind, um die Etappenziele zu erreichen. Der effiziente Ablauf spiegelt sich auch in den Finanzen wieder, da zusätzlicher Zeitaufwand für Fehlerkorrekturen
				verhindert wird. Zudem werden Teilaufgaben nicht vergessen. Je nach Projektauswahl kann auch eine hybride Lösung aus klassischem und agilem Projektmanagement eine Chance sein.</p>
			<p><span class="empty-space-para"></span>Das Projektmanagement befindet sich im Wandel und sollte weiterentwickelt werden. Genau dieser Wandel macht die Arbeitswelt spannend und faszinierend.
				Durch die Digitalisierung, z.B. Building Information Modeling, wird sich die Projektbearbeitung im Bereich Elektrotechnik in Planung und Ausführung im Lauf der nächsten Jahre noch mehrmals
				transformieren. Die agile Bearbeitung soll eine Möglichkeit aufzeigen, die man sofort anwenden und umsetzen kann.</p>
