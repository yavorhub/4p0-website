		<div class="article-title" style="">Agiles Projektmanagement verbessert die Projektbearbeitung</div>
		<div class="article-column-container" style="" >
			<div class="article-subtitle" style="margin-bottom: 5%;  font-family: Avenir; ">
				Heutige Bauprojekte sind nicht nur kompliziert, sondern auch dynamisch, verändern sich also während des Bauablaufs. Dies ruft nach einer Anpassung des Projektmanagements.
			</div>
			<div class="blog-article" style="">
				<p><span class="green-dash-article"></span>Bearbeitungstempo und Kostendruck erhöhen die Belastung von Projektverantwortlichen.
				«Scrum» als Unterform des sog. «agilen» Projektmanagements ist ein Lösungsansatz und hat der IT-Branche zu einer schnellen, flexiblen
				Bearbeitung von Projektaufgaben verholfen. Die erste Frage, die Sie sich jetzt stellen werden, ist sicherlich: «Kann ein IT-Projekt mit
					 einem Bauprojekt verglichen werden?» Die Antwort lautet: Die Anforderungen und die Probleme in der Projektbearbeitung sind dieselben.</p>
				<p class="paragraph-heading-alpha">Dem höheren Terminund Kostendruck begegnen</p>Das klassische Projektmanagement hat sich der in den
				letzten Jahren erschwerten Ablauf- und Kostensituation nicht angepasst. Das Festhalten an etablierten Teamstrukturen und den darin verankerten Abläufen
				verlängert die Reaktionszeiten. Die zentrale Herausforderung ist neben den Kosten der hohe Termindruck. Oft beeinträchtigt dieser den Blick auf das Wesentliche: auf
				Überwachung und Analyse des Projektfortschritts.
				</p><p><span class="empty-space-para"></span>Aus Sicht eines einzelnen am Bau beteiligten Unternehmens, z.B. der Elektroplanung, erschwert zudem die Abhängigkeit
				von den anderen am Bau beteiligten Gewerken und von weiteren externen Einflüssen wie z.B. veränderten Kunden- wünschen oder behördlichen Auflagen die effiziente Bearbeitung des
					Projekts, denn offene Fragen lassen sich teilweise erst im vereinbarten Sitzungsraster besprechen statt sofort.</p>
					<p><span class="empty-space-para"></span>Heute werden Projekte häufig nach dem «Wasserfallmodell» aus dem Jahr 1950 bearbeitet, das heisst, das Vorgehen ist linear: Initiierung – Planung
						– Ausfüh- rung – Abschluss. Jede Phase hat einen vordefinierten Start- und Endpunkt. Dieser Ablauf ergibt Sinn, wenn Anforderungen, Leistungen und Abläufe präzise be- schrieben werden können.
						Doch fallen Projekte zwischen den Phasen Planung und Ausführung nicht immer wieder zurück? Das ist unerwünscht, denn damit verzögert und verteuert sich ein Projekt.</p>
				<p class="paragraph-heading-alpha">Agil statt schwerfällig</p>
				Beim agilen Projektmanagement ist die Leistung variabel, dafür sind der Termin und der Aufwand fest definiert. Dies ermöglicht schnelles Handeln bei sich verändernden Prozessen oder Kundenwünschen.
				Agiles Projektmanagement wird nicht als Modell verstanden, das ein konkretes Vorgehen beschreibt. Vielmehr bildet es einen Rahmen für agile Prozesse. Um den Anwendern grösstmögliche Freiheit in der
				Umsetzung einzuräumen, wer- den keine konkreten Techniken vorgegeben, sondern Rollen und Abläufe.
				<p><span class="empty-space-para"></span>Agiles Projektmanagement wie Scrum kennt drei zentrale Rollen:
					 <ul style="margin-left: 1%;">
						 <li>– Der Produktverantwortliche ist Fachex</li>
						 <li>– perte und verantwortlich für die Umsetzung der Anforderungen und Kundenwünsche.</li>
						 <li>– Der Srcum-Master ist zuständig für die Einhaltung der Prozesse.</li>
						 <li>– Das Team ist die dritte Rolle. Die klassi- sche Verteilung wird aufgebrochen, der Projektleiter wird zum Teammitglied und übernimmt die Rolle eines Moderators.</li>
					 </ul>
				Der Ablauf in der Projektplanung wird nur auf den gerade wichtigsten nächsten Schritt, den sog. «Sprint», reduziert. Der Fokus liegt dabei auf drei Punkten:
				<ul style="margin-left: 1%;">
					<li>– Transparenz: Für alle Teammitglieder ist jederzeit ersichtlich, welche Sprints erledigt wurden und welche als nächste anstehen.</li>
					<li>– Überprüfung: Der aktuelle Projekt- stand und das weitere Vorgehen werden regelmässig überprüft.</li>
					<li>– Anpassung: Korrekturen sind kontinuierlich möglich. Zusätzliche Kunden wünsche können jederzeit eingeflochten werden.</li>
				</ul>
				
